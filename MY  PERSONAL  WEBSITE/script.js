
// Function to handle language change
function changeLanguage(language) {
    if (language === 'en') {
        window.location.href = 'index.html'; // Redirect to the English version
    } else if (language === 'uz') {
        window.location.href = 'index-uz.html'; // Redirect to the Uzbek version
    } else if (language === 'ru') {
        window.location.href = 'index-ru.html'; // Redirect to the Russian version
    }
}




